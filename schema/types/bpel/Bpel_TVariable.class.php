	{
		$this->messageType = $messageType;
	}
	

	public function getType()
	{
		return $this->type;
	}
	public function setType($type)
	{
		$this->type = $type;
	}
	

	public function getElement()
	{
		return $this->element;
	}
	public function setElement($element)
	{
		$this->element = $element;
	}
	
